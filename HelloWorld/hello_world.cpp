#include <iostream>  // Include the standard I/O stream library

int main() {  
    // The main function is the entry point of a C++ program

    std::cout << "Hello, World!" << std::endl;
    // std::cout is used to print output to the console.
    // "Hello, World!" is a string literal stored in the read-only data segment.
    // std::endl moves the cursor to the next line (similar to '\n').

    return 0;  
    // Returning 0 indicates that the program executed successfully.
}
